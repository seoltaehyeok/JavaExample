package Ramda_andthen_compose;

public class Ramda_Address {
	private String country;
	private String city;
	
	public Ramda_Address(String country, String city) {
		this.country = country;
		this.city = city;
	}

	public String getCountry() {
		return country;
	}

	public String getCity() {
		return city;
	}
	
	
}
